u32 MemoryCard::calcChecksum(void* dataptr, u32 length)
{
	u32 sum = 0x32546532;
	for (int i = 0; i < length >> 2; i++) {
		u8 i2   = i;
		u32 val = *((u32*)dataptr + i);
		sum += (i2 << 24) | (((i2 + 1) & 0xFF) << 16) | (((i2 - 1) & 0xFF) << 8) | ((i2 + 2) & 0xFF);
		sum += val;
	}
	sum = 0x32546532 - sum;
	return sum;
}


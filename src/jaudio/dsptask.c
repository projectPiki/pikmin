static void DspHandShake(void* a)
{
	volatile u32 mail;
	STACK_PAD_VAR(2);

	do {
		mail = DSPCheckMailFromDSP();
	} while (mail == 0);
	DSPReadMailFromDSP();
	DSPCheckMailFromDSP();
	Dsp_Running_Start();
}

